// Merge: use complete translations where available, fall back to existing
function mergeDict(existing, complete) {
  const result = {};
  // Start with all keys from Italian reference
  for (const key of Object.keys(itPairs)) {
    if (complete[key]) {
      result[key] = complete[key];
    } else if (existing[key]) {
      result[key] = existing[key];
    } else {
      // Fall back to English
      result[key] = enPairs[key] || itPairs[key];
    }
  }
  return result;
}

