/**
 * Script to complete all missing translations for the Memoro project.
 * Reads the current translations.ts, fills in missing keys for all 10 languages,
 * and writes the complete file back.
 */

const fs = require('fs');
const path = require('path');

// Read the current file
const filePath = path.join(__dirname, '..', 'src', 'lib', 'i18n', 'translations.ts');
const content = fs.readFileSync(filePath, 'utf8');

// Extract a dictionary from the file content
function extractDict(content, langName) {
  const start = content.indexOf('const ' + langName + ': TranslationDict = {');
  if (start === -1) return {};
  const end = content.indexOf('};', start);
  const block = content.substring(start, end);
  const regex = /"([^"]+)":\s*"([^"]*)"/g;
  let match;
  const pairs = {};
  while ((match = regex.exec(block)) !== null) {
    pairs[match[1]] = match[2];
  }
  return pairs;
}

