// Generate dict source code
function dictToSource(dict, varName) {
  const entries = Object.entries(dict)
    .map(([key, value]) => `  "${key}": "${value.replace(/\\/g, '\\\\').replace(/"/g, '\\"')}"`)
    .join(',\n');
  return `const ${varName}: TranslationDict = {\n${entries}\n};`;
}

