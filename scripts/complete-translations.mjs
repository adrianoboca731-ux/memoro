const lines = content.split('\n');
let currentLang = null;
let dicts = {};

for (const line of lines) {
  const langMatch = line.match(/^const (\w+): TranslationDict/);
  if (langMatch) {
    currentLang = langMatch[1];
    dicts[currentLang] = {};
  } else if (currentLang && line.match(/^\};/)) {
    currentLang = null;
  } else if (currentLang) {
    const kvMatch = line.match(/\"([^\"]+)\": \"(.*)\"/);
    if (kvMatch) {
      dicts[currentLang][kvMatch[1]] = kvMatch[2];
    }
  }
}

const itKeys = Object.keys(dicts.it);
console.log(`Italian reference has ${itKeys.length} keys`);

// Generate the complete translations file
function generateLangBlock(langVar, langName, dict) {
  let output = `const ${langVar}: TranslationDict = {\n`;
  // Order keys same as Italian
  for (const key of itKeys) {
    const val = dict[key];
    if (val === undefined) {
      console.error(`  WARNING: Missing key "${key}" in ${langName}`);
    }
    // Escape any double quotes and backslashes in the value
    const escaped = (val || '').replace(/\\/g, '\\\\').replace(/"/g, '\\"');
    output += `  "${key}": "${escaped}",\n`;
  }
  output += `};\n`;
  return output;
}

