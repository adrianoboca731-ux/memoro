// Helper function to generate TypeScript dict string from a JS object
function dictToTS(obj) {
  const entries = Object.entries(obj);
  return entries.map(([key, value]) => {
    // Escape backslashes and double quotes in values
    const escapedValue = value.replace(/\\/g, '\\\\').replace(/"/g, '\\"');
    return `  "${key}": "${escapedValue}"`;
  }).join(',\n');
}

// Replace each language section in the file
function replaceLangSection(content, varName, newDict) {
  // Match the const declaration: const VARNAME: TranslationDict = { ... };
  const regex = new RegExp(
    `const ${varName}: TranslationDict = \\{[\\s\\S]*?\\};`,
    'm'
  );
  const replacement = `const ${varName}: TranslationDict = {\n${dictToTS(newDict)}\n};`;
  const newContent = content.replace(regex, replacement);
  if (newContent === content) {
    console.error(`ERROR: Could not find section for ${varName}`);
    return content;
  }
  console.log(`✓ Replaced ${varName} section (${Object.keys(newDict).length} keys)`);
  return newContent;
}

